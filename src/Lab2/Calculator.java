package Lab2;

public class Calculator {
    // "+"
    public double addition(int a, int b) {
        return a + b;
    }

    public double addition(double a, double b) {
        return a + b;
    }

    public double addition(long a, long b) {
        return a + b;
    }

    // "-"
    public double subtraction(int a, int b) {
        return a - b;
    }

    public double subtraction(double a, double b) {
        return a - b;
    }

    public double subtraction(long a, long b) {
        return a - b;
    }

    // "*"
    public double multiplication(int a, int b) {
        return a * b;
    }

    public double multiplication(double a, double b) {
        return a * b;
    }

    public double multiplication(long a, long b) {
        return a * b;
    }

    // "/"
    public double division(int a, int b) {
        return a / b;
    }

    public double division(double a, double b) {
        return a / b;
    }

    public double division(long a, long b) {
        return a / b;
    }
}
